#!/usr/bin/env node
/**
 * build-discovery.mjs
 *
 * Generates the three "discovery" files at the site root:
 *
 *   sitemap.xml — every public page from pages.config.mjs (uses each .html
 *                 file's mtime as <lastmod>).
 *   robots.txt  — allow-all crawlers, point to the sitemap.
 *   llms.txt    — the emerging standard AI assistants (ChatGPT, Claude,
 *                 Perplexity, etc.) use to understand a site at a glance.
 *                 Structured Markdown with sections per page group.
 *
 * Idempotent. Re-run any time pages.config.mjs changes:
 *
 *     node tools/build-discovery.mjs
 */

import { readFile, writeFile, readdir, stat } from 'node:fs/promises';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { SITE, PAGES } from './pages.config.mjs';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const baseUrl = SITE.url.replace(/\/+$/, '');

function canonicalFor(filename) {
  if (filename === 'index.html') return `${baseUrl}/`;
  return `${baseUrl}/${filename}`;
}

// ─── Discover pages (config order, skipping noindex) ─────────────────────────

const pageEntries = Object.entries(PAGES).filter(([, page]) => !page.noindex);

// ─── sitemap.xml ─────────────────────────────────────────────────────────────

// Priority/changefreq hints by type — search engines treat these as advisory.
const PRIORITY = {
  home: { p: '1.0', f: 'weekly' },
  about: { p: '0.8', f: 'monthly' },
  contact: { p: '0.8', f: 'monthly' },
  service: { p: '0.9', f: 'monthly' },
  industry: { p: '0.8', f: 'monthly' },
  auctionProduct: { p: '0.7', f: 'monthly' },
  clientShowcase: { p: '0.6', f: 'monthly' },
  case: { p: '0.7', f: 'monthly' },
  blog: { p: '0.7', f: 'weekly' },
  blogPost: { p: '0.6', f: 'monthly' },
  portfolio: { p: '0.7', f: 'monthly' },
  testimonial: { p: '0.5', f: 'monthly' },
  policy: { p: '0.3', f: 'yearly' },
  page: { p: '0.5', f: 'monthly' },
};

const urlEntries = [];
for (const [file, page] of pageEntries) {
  const path = join(ROOT, file);
  let lastmod;
  try {
    lastmod = (await stat(path)).mtime.toISOString().slice(0, 10);
  } catch {
    continue;
  }
  const meta = PRIORITY[page.type] ?? PRIORITY.page;
  urlEntries.push(
    `  <url>\n    <loc>${canonicalFor(file)}</loc>\n    <lastmod>${lastmod}</lastmod>\n    <changefreq>${meta.f}</changefreq>\n    <priority>${meta.p}</priority>\n  </url>`,
  );
}

const sitemap =
  `<?xml version="1.0" encoding="UTF-8"?>\n` +
  `<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n` +
  urlEntries.join('\n') +
  `\n</urlset>\n`;

await writeFile(join(ROOT, 'sitemap.xml'), sitemap, 'utf8');

// ─── robots.txt ──────────────────────────────────────────────────────────────

const robots =
  `# robots.txt — generated by tools/build-discovery.mjs\n` +
  `User-agent: *\n` +
  `Allow: /\n` +
  `\n` +
  `# Disallow heavy build/tooling directories from crawl\n` +
  `Disallow: /tools/\n` +
  `\n` +
  `Sitemap: ${baseUrl}/sitemap.xml\n`;

await writeFile(join(ROOT, 'robots.txt'), robots, 'utf8');

// ─── llms.txt ────────────────────────────────────────────────────────────────
//
// Spec: https://llmstxt.org/ — a Markdown index that AI assistants can use
// to understand site structure without crawling the full HTML.

function group(filterFn) {
  return pageEntries
    .filter(([file, page]) => filterFn(file, page))
    .map(([file, page]) => `- [${page.title}](${canonicalFor(file)}): ${page.description}`)
    .join('\n');
}

const llmsLines = [
  `# ${SITE.legalName}`,
  '',
  `> ${SITE.description} Headquartered in Kathmandu, Nepal with a presence in Melbourne, Australia. Serving clients globally since ${SITE.founded}.`,
  '',
  'Use this file to discover the site\'s structure and the canonical URL for each page. Each link below points to the production page on ' + baseUrl + '.',
  '',
  '## Company',
  '',
  // 'page' captures Team and Security & Compliance — company/trust pages that
  // would otherwise silently drop out of llms.txt.
  group((f, p) => ['home', 'about', 'contact', 'page'].includes(p.type)),
  '',
  '## Services',
  '',
  group((_, p) => p.type === 'service'),
  '',
  '## Industries',
  '',
  group((_, p) => p.type === 'industry'),
  '',
  '## Auction software products',
  '',
  group((_, p) => p.type === 'auctionProduct'),
  '',
  '## Client showcases',
  '',
  group((_, p) => p.type === 'clientShowcase'),
  '',
  '## Case studies',
  '',
  group((_, p) => p.type === 'case'),
  '',
  '## Portfolio & social proof',
  '',
  group((_, p) => ['portfolio', 'testimonial'].includes(p.type)),
  '',
  '## Blog',
  '',
  group((_, p) => ['blog', 'blogPost'].includes(p.type)),
  '',
  '## Legal',
  '',
  group((_, p) => p.type === 'policy'),
  '',
  '## Contact',
  '',
  ...SITE.locations.map((loc) => {
    const parts = [loc.name];
    if (loc.streetAddress) parts.push(loc.streetAddress);
    if (loc.addressLocality) parts.push(loc.addressLocality);
    if (loc.addressRegion) parts.push(loc.addressRegion);
    if (loc.addressCountry) parts.push(loc.addressCountry);
    return `- ${parts.join(', ')} — ${loc.telephone}`;
  }),
  '',
  '## Social',
  '',
  ...SITE.social.map((u) => `- ${u}`),
  '',
];

await writeFile(join(ROOT, 'llms.txt'), llmsLines.join('\n'), 'utf8');

console.log(
  `build-discovery: wrote sitemap.xml (${urlEntries.length} urls), robots.txt, llms.txt`,
);
